import React, { useState } from "react";
import { Frown, Search, Zap } from "lucide-react";
import { Link, useNavigate } from "react-router-dom";

const Navbar = ({handleSearch}) => {

  const navigate = useNavigate();
  const [input, setinput] = useState("");

  const searchhandleSearch = (e) => {
    e.preventDefault();
    if (input.trim()) {
      handleSearch(input.trim());
      navigate(`search/${input}`);
      setinput(" ");
    }
  };

  return (
    <>
      <nav className="sticky to-0 z-50 bg-gray-950/90 backdrop:blur-md shadow-2xl shadow-black/50 border-b border-blue-900/50 ">
        <div className="max-w-8xl mx-auto px-4 sm:px-6 lg:px-8">
          <div className=" flex justify-between items-center h-16">
            <Link
              to={"/"}
              className="flex items-center text-2xl font-black text-white hover:text-blue-400 transition duration-300 tracking-widest"
            >
              <Zap className="w-7 h-7 mr-2 text-yellow-400 fill-yellow-400/20" />
              <span className="text-blue-400">Pro</span> Chef
            </Link>

            <form
              onSubmit={searchhandleSearch}
              className=" flex-1 max-w-lg mx-4 hidden sm:flex"
            >
              <input
                type="text"
                value={input}
                onChange={(e) => setinput(e.target.value)}
                placeholder="Search dishes, ingredienrs, or cuisine"
                className="w-full px-5 py-2 border border-green-700 bg-gray-900 text-gray-50 rounded-l-full focus:outline-none focus:ring-4 focus:ring-blue-600/50 transition "
              ></input>
              <button
                type="submit"
                className="bg-linear-to-r from-blue-600 to-cyan-500 text-white p-2.2 rounded-r-full hover:from-blue-700 hover:to-cyan-600 transition duration-300 shadow-lg shadow-blue-800/50 hover:shadow-xl hover:shadow-blue-800/90"
              >
                <Search className="w-5 h-5" />
              </button>
            </form>
          </div>
        </div>
      </nav>
    </>
  );
};

export default Navbar;
